package org.example.task2;

public class Pair {

    Integer value1;
    Integer value2;

    public Pair(Integer value1, Integer value2) {
        this.value1 = value1;
        this.value2 = value2;
    }

    public Integer getValue1() {
        return value1;
    }

    public Integer getValue2() {
        return value2;
    }
}
